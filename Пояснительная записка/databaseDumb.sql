DROP DATABASE IF EXISTS `post_database`;
CREATE DATABASE IF NOT EXISTS `post_database`;
USE `post_database`;

DROP TABLE IF EXISTS `Газеты и журналы`;
CREATE TABLE `Газеты и журналы` (
  `Код` INTEGER NOT NULL AUTO_INCREMENT, 
  `Индекс` INTEGER NOT NULL DEFAULT 0, 
  `Название` VARCHAR(100) NOT NULL, 
  `Подписная_цена(мес)` DECIMAL(19,4) NOT NULL DEFAULT 0, 
  PRIMARY KEY (`Код`)
) ENGINE=innodb DEFAULT CHARSET=utf8;
SET autocommit=1;

DROP TABLE IF EXISTS `Почтальоны`;
CREATE TABLE `Почтальоны` (
  `Код` INTEGER NOT NULL AUTO_INCREMENT, 
  `Фамилия` VARCHAR(30) NOT NULL, 
  `Имя` VARCHAR(30) NOT NULL, 
  `Отчество` VARCHAR(30) NOT NULL, 
  `Телефон` VARCHAR(20) NOT NULL, 
  `Паспорт` VARCHAR(30) NOT NULL, 
  `Работает_с` DATE NOT NULL, 
  PRIMARY KEY (`Код`)
) ENGINE=innodb DEFAULT CHARSET=utf8;
SET autocommit=1;

DROP TABLE IF EXISTS `Участок`;
CREATE TABLE `Участок` (
  `Участок` VARCHAR(255) NOT NULL, 
  `Почтальон` INTEGER NOT NULL, 
  PRIMARY KEY (`Участок`),
  FOREIGN KEY (`Почтальон`) REFERENCES `Почтальоны` (`Код`)
) ENGINE=innodb DEFAULT CHARSET=utf8;
SET autocommit=1;

DROP TABLE IF EXISTS `Подписчик`;
CREATE TABLE `Подписчик` (
  `Код` INTEGER NOT NULL AUTO_INCREMENT, 
  `Фамилия` VARCHAR(30) NOT NULL, 
  `Имя` VARCHAR(30) NOT NULL, 
  `Отчество` VARCHAR(30) NOT NULL, 
  `Домашний_адрес` VARCHAR(255) NOT NULL, 
  `Участок` VARCHAR(255) NOT NULL, 
  PRIMARY KEY (`Код`),
  FOREIGN KEY (`Участок`) REFERENCES `Участок` (`Участок`)
) ENGINE=innodb DEFAULT CHARSET=utf8;
SET autocommit=1;

DROP TABLE IF EXISTS `Подписки`;
CREATE TABLE `Подписки` (
  `Код` INTEGER NOT NULL AUTO_INCREMENT, 
  `Подписчик` INTEGER NOT NULL, 
  `Издание` INTEGER NOT NULL, 
  `Дата_подписки` DATE NOT NULL, 
  `Срок(мес)` INTEGER NOT NULL DEFAULT 0, 
  PRIMARY KEY (`Код`),
  FOREIGN KEY (`Подписчик`) REFERENCES `Подписчик` (`Код`),
  FOREIGN KEY (`Издание`) REFERENCES `Газеты и журналы` (`Код`)
) ENGINE=innodb DEFAULT CHARSET=utf8;
SET autocommit=1;



